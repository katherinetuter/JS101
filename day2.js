//switch statement

var day = "Friday";

switch(day){
	case "Monday":
		console.log("Booo");
		break;
	case "Friday":
		console.log("party");
		break;
	default: 
		console.log("not Monday");
}

if(day === "Monday"){
	console.log("Booo");
}else if(day === "Friday"){
console.log("party");	
} else{
console.log("not Monday");
}
